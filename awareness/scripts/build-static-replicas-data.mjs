// Regenerate js/newsletter/static_replicas_data.js — the 7 static "design
// replica" poster documents (templates/reference/pipeline/*.html) inlined as JS
// strings so static_replicas.js can render them with NO fetch().
//
// Why: those builders fetch() their HTML from a sibling file. fetch() of a local
// file is blocked under the file:// protocol, so the moment the app is opened by
// double-clicking index.html from a zip (no server), every static replica stays
// stuck on the "Loading static template…" notice. Baking the HTML into a
// committed bundle the builder reads synchronously makes them render everywhere
// (http, file://, and from a zip) — the same fix build-template-assets.mjs uses
// for the brand images.
//
//   npm run build:static-replicas
//
// Run this after editing any templates/reference/pipeline/*.html replica.

import { readFile, writeFile } from 'node:fs/promises';
import path from 'node:path';
import { fileURLToPath } from 'node:url';

const ROOT = path.resolve(path.dirname(fileURLToPath(import.meta.url)), '..');
const OUT = path.join(ROOT, 'js', 'newsletter', 'static_replicas_data.js');

// id -> source file. MUST stay in sync with REPLICAS in js/newsletter/static_replicas.js.
const REPLICAS = {
  // gen_vishing and gen_social_engineering are now dynamic templates
  // (core_templates.js), not static replicas.
  gen_phonescam:          'templates/reference/pipeline/phonescam_replica_light.html',
  gen_right_message:      'templates/reference/pipeline/right_message_replica.html',
  gen_spear_phishing:     'templates/reference/pipeline/spear_phishing_replica.html',
  gen_weakest_link:       'templates/reference/pipeline/weakest_link_replica_light.html',
  gen_wifi_safety:        'templates/reference/pipeline/wifi_safety_replica.html',
};

const parts = [];
let totalChars = 0;
for (const [id, rel] of Object.entries(REPLICAS)) {
  const html = await readFile(path.join(ROOT, rel), 'utf8');
  totalChars += html.length;
  // JSON.stringify yields a valid JS string literal with every quote, newline,
  // backslash and control char escaped. Safe inside an EXTERNAL <script src> —
  // the </script> close-tag rule only applies to inline scripts, not .js files.
  parts.push('    ' + JSON.stringify(id) + ': ' + JSON.stringify(html) + ',');
}

const banner =
`// AUTO-GENERATED by scripts/build-static-replicas-data.mjs — do not hand-edit.
// The 7 static design-replica posters inlined as strings so static_replicas.js
// renders them with no fetch() (works from a zip opened via file://).
// Regenerate after editing any templates/reference/pipeline/*.html:
//   npm run build:static-replicas
`;

const body =
`${banner}(function () {
  'use strict';
  window.App = window.App || {};
  window.App.StaticReplicaData = {
${parts.join('\n')}
  };
})();
`;

await writeFile(OUT, body, 'utf8');
console.log(`Wrote ${path.relative(ROOT, OUT)} — ${parts.length} replicas, ${(totalChars / 1024).toFixed(0)} KB of HTML.`);
