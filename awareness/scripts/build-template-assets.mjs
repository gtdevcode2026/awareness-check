// Regenerate assets/template_assets.js — the brand/hero images used by the
// generated templates (gen_chase_email, gen_cybershield) embedded as data URIs.
//
// Why: the templates used relative `assets/X.png` <img> srcs. Those break the
// moment the generated HTML leaves the project folder — a downloaded standalone
// file, an email, or the app opened straight from a zip via file:// (where
// fetch()-based inlining is also blocked). Baking the images into a committed
// data-URI bundle the builder reads at build time makes every generated
// newsletter self-contained (http, file://, and email all render the logos).
//
//   npm run build:template-assets
//
// Run this after replacing any of the listed brand assets.

import { readFile, writeFile } from 'node:fs/promises';
import path from 'node:path';
import { fileURLToPath } from 'node:url';

const ROOT = path.resolve(path.dirname(fileURLToPath(import.meta.url)), '..');
const ASSETS_DIR = path.join(ROOT, 'assets');
const BUNDLE = path.join(ASSETS_DIR, 'template_assets.js');

// The images referenced by the generated templates (js/newsletter/core_templates.js).
const FILES = ['ABI.png', 'mascot_mug.png', '4.png', 'strong_passwords_hero.jpg', 'vishing_hero.jpg',
  // Vishing poster icon badges (rasterized from SVG by build-vishing-icons.mjs;
  // raster so they render in Outlook/Gmail, where inline SVG is dropped).
  'vishing_ico_time.png', 'vishing_ico_emotion.png', 'vishing_ico_verify.png', 'vishing_ico_context.png',
  // gen_social_engineering: line-art hero figure + raster red-flag row icon.
  'social_engineering_hero.png', 'redflag_ico.png',
  // newspaper (The Cyber Gazette): hero images for the per-article image slots
  // (big lead image = temp_img; small secondary windows = genhts; image223 spare).
  'image223.jpeg', 'temp_img.jpeg', 'genhts.jpeg'];

const MIME = {
  '.png': 'image/png',
  '.jpg': 'image/jpeg',
  '.jpeg': 'image/jpeg',
  '.gif': 'image/gif',
  '.webp': 'image/webp',
  '.svg': 'image/svg+xml'
};

const entries = [];
const skipped = [];
for (const name of FILES) {
  try {
    const bytes = await readFile(path.join(ASSETS_DIR, name));
    const mime = MIME[path.extname(name).toLowerCase()] || 'application/octet-stream';
    entries.push(`  ${JSON.stringify(name)}: ${JSON.stringify(`data:${mime};base64,${bytes.toString('base64')}`)}`);
  } catch {
    skipped.push(name);
  }
}

const js =
  '/* eslint-disable */\n' +
  '/* AUTO-GENERATED by scripts/build-template-assets.mjs — do not edit by hand.\n' +
  '   Brand/hero images embedded as data URIs so generated newsletters are\n' +
  '   self-contained over http, file://, and email. Regenerate: npm run build:template-assets */\n' +
  'window.App = window.App || {};\n' +
  'window.App.TemplateAssets = {\n' +
  entries.join(',\n') + (entries.length ? '\n' : '') +
  '};\n';

await writeFile(BUNDLE, js, 'utf8');
console.log(`[template-assets] wrote assets/template_assets.js with ${entries.length} image(s).`);
if (skipped.length) console.warn(`[template-assets] skipped (missing): ${skipped.join(', ')}`);
